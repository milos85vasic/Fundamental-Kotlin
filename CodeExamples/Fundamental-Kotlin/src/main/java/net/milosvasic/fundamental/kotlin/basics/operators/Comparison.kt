package net.milosvasic.fundamental.kotlin.basics.operators

fun main() {

    val a = 1
    val b = 2
    val c = 1

    println("a: $a, b: $b, c: $c")
    println("a == b: ${a == b}")
    println("a == c: ${a == c}")
    println("a != b: ${a != b}")
    println("a != c: ${a != c}")

    println("a > b: ${a > b}")
    println("a > c: ${a > c}")
    println("a < b: ${a < b}")
    println("a < c: ${a < c}")

    println("a >= b: ${a >= b}")
    println("a >= c: ${a >= c}")
    println("a <= b: ${a <= b}")
    println("a <= c: ${a <= c}")
}